    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

       int selectedRow = tblFaculty.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Please select a faculty member to delete!",
            "No Selection",
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String facultyId = tblFaculty.getValueAt(selectedRow, 0).toString();
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
        "Are you sure you want to delete faculty " + facultyId + "?",
        "Confirm Delete",
        javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        boolean deleted = business.getEmployeeDirectory().deleteEmployee(facultyId);
        if (deleted) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Faculty deleted successfully!",
                "Success",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            populateTable();
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed


