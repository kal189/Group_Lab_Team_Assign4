    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

      String searchType = (String) cmbSearchType.getSelectedItem();
    String searchText = txtSearch.getText().trim();

    if (searchText.isEmpty()) {
        populateTable();
        return;
    }

    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel) tblFaculty.getModel();
    model.setRowCount(0);

    for (EmployeeProfile faculty : business.getEmployeeDirectory().getEmployeeList()) {
        boolean match = false;
        String personId = faculty.getPerson().getPersonId();

        if (searchType.equals("Faculty ID") && personId.toLowerCase().contains(searchText.toLowerCase())) {
            match = true;
        } else if (searchType.equals("Name") && personId.toLowerCase().contains(searchText.toLowerCase())) {
            match = true;
        } else if (searchType.equals("Department")) {
            match = true;
        }

      if (match) {
    Object[] row = new Object[4];
    row[0] = faculty.getPerson().getPersonId();
    row[1] = faculty.getPerson().getName();
    row[2] = "Information Systems";
    row[3] = faculty.getPerson().getEmail();
    model.addRow(row);
      }
    }
    }//GEN-LAST:event_btnSearchActionPerformed

