private void populateProfileData() {
    if (currentUser != null) {
        txtUserId.setText(currentUser.getPersonId());
        txtUsername.setText(currentUser.getUserLoginName());
        txtName.setText(currentUser.getAssociatedPersonProfile().getPerson().getName());
        txtEmail.setText(currentUser.getAssociatedPersonProfile().getPerson().getEmail());
        txtRole.setText(currentUser.getRole());
    }
}

private void makeFieldsReadOnly() {
    txtUserId.setEditable(false);
    txtUsername.setEditable(false);
    txtName.setEditable(false);
    txtEmail.setEditable(false);
    txtRole.setEditable(false);
}

