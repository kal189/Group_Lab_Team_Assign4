    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:

     if (btnEdit.getText().trim().equals("Edit")) {
        txtName.setEditable(true);
        txtEmail.setEditable(true);
        btnEdit.setText("Save Changes");
    } else {
        currentUser.getAssociatedPersonProfile().getPerson().setName(txtName.getText());
        currentUser.getAssociatedPersonProfile().getPerson().setEmail(txtEmail.getText());
        
        javax.swing.JOptionPane.showMessageDialog(this,
            "Profile updated successfully!",
            "Success",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        txtName.setEditable(false);
        txtEmail.setEditable(false);
        btnEdit.setText("Edit ");
    }
    }//GEN-LAST:event_btnEditActionPerformed


