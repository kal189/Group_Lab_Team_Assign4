private void populateStudentData() {
    if (selectedStudent != null) {
        txtStudentId.setText(selectedStudent.getPerson().getPersonId());
        txtName.setText(selectedStudent.getPerson().getName());
        txtDepartment.setText("Information Systems");
        txtEmail.setText(selectedStudent.getPerson().getEmail());
        txtStatus.setText("Active");
    }

}

private void makeFieldsReadOnly() {
    txtStudentId.setEditable(false);
    txtName.setEditable(false);
    txtDepartment.setEditable(false);
    txtEmail.setEditable(false);
    txtStatus.setEditable(false);
}

