    private void btnSubmitAssignmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitAssignmentActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblCoursework.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a course to submit assignment.");
        return;
    }
    String courseId = (String) tblCoursework.getValueAt(selectedRow, 0);
    assignmentSubmitted.put(courseId, true);
    JOptionPane.showMessageDialog(this, "Assignment submitted for " + courseId + "!");
    populateTable();
    updateGPA();
    }//GEN-LAST:event_btnSubmitAssignmentActionPerformed


