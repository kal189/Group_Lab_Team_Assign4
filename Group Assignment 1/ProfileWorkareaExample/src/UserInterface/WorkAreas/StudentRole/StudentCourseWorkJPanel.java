    private void populateTable() {
          javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblCoursework.getModel();
    model.setRowCount(0);

    info5100.university.example.CourseSchedule.CourseLoad courseLoad = universityStudent.getCurrentCourseLoad();
    if (courseLoad == null) return;

    for (info5100.university.example.CourseSchedule.SeatAssignment seat : courseLoad.getSeatAssignments()) {
       info5100.university.example.CourseCatalog.Course course = seat.getCourseOffer().getSubjectCourse();

        String courseId = course.getCOurseNumber();  
        String courseName = "Course " + course.getCOurseNumber();

        String status = assignmentSubmitted.getOrDefault(courseId, false) ? "Submitted" : "Pending";
        String grade = seat.GetCourseStudentScore() == 0 ? "Not Graded" :
                       String.valueOf(seat.GetCourseStudentScore());
        model.addRow(new Object[]{courseId, courseName, status, grade});
    }
}
    private void updateGPA() {
    info5100.university.example.CourseSchedule.CourseLoad courseLoad = universityStudent.getCurrentCourseLoad();
    if (courseLoad == null) {
        lblGPA.setText("Current GPA: N/A");
        return;
    }

    float totalScore = courseLoad.getSemesterScore();
    float totalCourses = courseLoad.getSeatAssignments().size();
    float gpa = totalCourses > 0 ? totalScore / totalCourses : 0;

    lblGPA.setText("Current GPA: " + String.format("%.2f", gpa));
}
